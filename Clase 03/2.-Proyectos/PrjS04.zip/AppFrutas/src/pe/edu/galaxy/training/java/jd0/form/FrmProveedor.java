    private void limpiar() {
        this.codigo=0;
        txtRazonSocial.setText("");
        txtRuc.setText("");
        txtRazonSocial.requestFocus();
    }
    private void insertar() {
        try {
            Proveedor proveedor = new Proveedor();
            proveedor.setRazonSocial(txtRazonSocial.getText());
            proveedor.setRuc(txtRuc.getText());
            if (proveedorDAO.insertar(proveedor)) {
                JOptionPane.showMessageDialog(rootPane,"Exito de insersiÃ³n" ,"Aviso",JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
                this.listar();
            } else {
                JOptionPane.showMessageDialog(rootPane,"Error de insersiÃ³n" ,"Aviso",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
     private void actualizar() {
        try {
            Proveedor proveedor = new Proveedor();
            proveedor.setCodigo(codigo);
            proveedor.setRazonSocial(txtRazonSocial.getText());
            proveedor.setRuc(txtRuc.getText());
            if (proveedorDAO.actualizar(proveedor)) {
                JOptionPane.showMessageDialog(rootPane,"Exito de actualizaciÃ³n" ,"Aviso",JOptionPane.INFORMATION_MESSAGE);
                this.limpiar();
                this.listar();
            } else {
                JOptionPane.showMessageDialog(rootPane,"Error de actualizaciÃ³n" ,"Aviso",JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
     
      private void eliminar() {
        try {
           if( JOptionPane.showConfirmDialog(rootPane,
                    "Confirma la eliminaciÃ³n de"+txtRazonSocial.getText() , 
                    "Alerta", JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE)==JOptionPane.OK_OPTION){
                if (proveedorDAO.eliminar(this.codigo)) {
                    JOptionPane.showMessageDialog(rootPane,"Exito de eliminaciÃ³n" ,"Aviso",JOptionPane.INFORMATION_MESSAGE);
                    this.limpiar();
                    this.listar();
                } else {
                    JOptionPane.showMessageDialog(rootPane,"Error de eliminaciÃ³n" ,"Aviso",JOptionPane.WARNING_MESSAGE);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

